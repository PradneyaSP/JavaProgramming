public class Introduction {

        public static void main(String[] args) {
            int p = 20, q = 10;
            int result;

            result=p+q;                                                 //Addition Operator
            System.out.println(result);

            System.out.println(p - q);                                 //Subtraction Operator

            System.out.println(p * q);                                 //Multiplication Operator

            System.out.println(p / q);                                 //Division Operator

            System.out.println(p % q);                                 //Modulus Operator

        }

    }
